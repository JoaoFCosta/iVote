  /** Povoar lista com as listas disponiveis para a a eleição atual e para o
   *  eleitor que fez login. */
  public void setListaListas () {
    Collection<String> listas = sge.opcoesVotoLegislativa(idEleicao, idCidadao);
    DefaultListModel dlm      = new DefaultListModel();
    
    // Associar o novo list model à lista.
    optionsList.setModel(dlm);
    
    for (String lista : listas)
      dlm.addElement(lista);
  }
  
