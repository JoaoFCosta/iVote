  /**
   * Registar o voto.
   */
  private void voteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_voteButtonActionPerformed
    String selecionado = (String) optionsList.getSelectedValue();
    int decisao = JOptionPane.showConfirmDialog(this,
        "Confirma que quer votar na lista: " + selecionado + "?");

    if (decisao == JOptionPane.YES_OPTION) {
      // TODO: Registar voto.
      
      // Voltar para o ecrã de Login.
      Login login = new Login();
      login.setLocationRelativeTo(this);
      this.dispose();
      login.setVisible(true);
    }
  }//GEN-LAST:event_voteButtonActionPerformed

  private void votoNuloButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_votoNuloButtonActionPerformed
    int d = JOptionPane.showConfirmDialog(this,
        "Tem a certeza que pretende fazer voto nulo?");

    if (d == JOptionPane.YES_OPTION) {
      // Registar voto nulo.
      sge.votoNuloLegislativa(idEleicao, idCidadao, false);

      // Regressar ao ecrã de Login.
      Login L = new Login();
      L.setLocationRelativeTo(this);
      this.dispose();
      L.setVisible(true);
    }
  }//GEN-LAST:event_votoNuloButtonActionPerformed

